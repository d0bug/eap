            	<li>
                	<h3>1. 如何开通PIV4.0权限？</h3>
                    <p>全职老师在入职时会同步开通4.0权限，不用申请。<br />兼职老师权限由李娜老师统一申请开通。</p>
                </li>
                <li>
                	<h3>2. 如何增加PIV4.0的学科权限？</h3>
                    <p>全职老师由本人发送邮件给洪莲，抄送教学主任、教研主管、师训组、杨天韧、张持召、陈志伟、李娜，教管组和排课组核实后开通。兼职老师由李娜老师统一申请开通。</p>
                </li>
                <li>
                	<h3>3. PIV4.0权限多长时间清理一次？</h3>
                    <p>每月会清理一次权限，某学科下所有学员都已结课或累计一个月未使用4.0的，该学科权限关闭。</p>
                </li>
                <li>
                	<h3>4. 讲义的下载次数有何要求？</h3>
                    <p>全职教师账号每天最多下载8讲，兼职教师账号每天最多下载4讲，同一讲次下教师版和学生版算作一讲，且同一讲次可无限下载。</p>
                </li>
                <li>
                	<h3>5. 讲义下载下来，但是打不开？</h3>
                    <p>请使用word2010或者word2013打开文档，低版本word或者WPS等兼容性差。</p>
                </li>
                <li>
                	<h3>6. 4.0的可视环节有哪几步？</h3>
                    <p>①备课即匹配课程完成时；②学生打指纹时；③生成课节报告时；④记录错题时。系统会自动触发信息至家长APP。</p>
                </li>
                <li>
                	<h3>7. 课时核录多次时间算作逾期？扣课时费么？</h3>
                    <p>课程结束后的48小时内未核录算作逾期，逾期时该次课的课时费全部扣除。系统会在24小时和46小时触发短信提醒老师核录课时。</p>
                </li>
                <li>
                	<h3>8. PIV4.0搭讲义能使用自己手里的题么？</h3>
                    <p>目前系统不支持自己导题，只能使用库里的试题，建议老师讲试题单独加在下载后讲义的最后面，或者课上单独补充。</p>
                </li>
                 <li>
                	<h3>9. 我有问题的话，怎么反馈？</h3>
                    <p>全职教师在“传道授业解惑”微信群里发信息，兼职教师在“高思VIP教师”微信群里发信息，最好有截图有真相。</p>
                </li>
                 <li>
                	<h3>10. PIV4.0使用起来太难了，看了说明还是不懂怎么办？</h3>
                    <p>请大胆的在微信群里呼喊，群里的PIV4.0小助手们会倾心远程指导，包教包会。</p>
                </li>
                 <li>
                	<h3>11. 为什么要使用4.0呢？一直搞不懂呀？</h3>
                    <p>用4.0记录学生学习轨迹生成课节报告，就好比在天猫购物一样，有你的购物记录和习惯，而线下超市就像3.0没有你的轨迹，不会用大数据推送，也不会推出你的偏好。课节报告有了，后续通过推送，孩子们会有很多增值服务，老师出测试卷和辅导方案也会非常轻松，基于历史数据系统几乎可以自动生成。高思投入大量成本精心研制4.0，希望能为学生创造价值，给孩子们更好的服务。4.0是教育未来的趋势，不用早晚被淘汰。</p>
                </li>
            </ul>
            
            <ul id="con_one_2"  style="display:none;">
            	<li>
                	<h3>1. 备课时，教研标准化讲义库没有讲义？</h3>
                    <p>使用较低版本的IE浏览器会出现此类情况，请使用火狐、谷歌或360极速浏览器。</p>
                </li>
                <li>
                	<h3>2. 筛选的目标讲义预览不了怎么办？</h3>
                    <p>第一步，重新刷新一遍；第二步，切换至相似内容的讲义替代；第三步，在微信群里反馈问题。</p>
                </li>
                <li>
                	<h3>3. 为何点击匹配课程后，无法匹配学员？</h3>
                    <p>第一步，先确认已给该学员排课，<br />第二步，核实该学员是否只剩下最后一次课，<br />第三步，如果还有问题，请在微信里反馈。</p>
                </li>
                <li>
                	<h3>4. 为什么有些选择题，在线上显示是完好的，一旦下载，答案就不见了？</h3>
                    <p>这是由于部分题的存储造成的，只能由教研员老师在题库中修改。所以，如果您发现有以上问题，请记录下该题的ID，或者直接拍照发到群中，会有教研员老师及时帮您处理。修改完毕后，请您重新保存讲义，之后下载，问题就解决了。</p>
                </li>
                <li>
                	<h3>5. 备完课，下载了讲义，看起来很好，但是打印时，（特别是答案部分）无法显示图片，该怎么解决？</h3>
                    <p>把讲义压缩为PDF文件，然后打印。</p>
                </li>
                <li>
                	<h3>6. 如果我备完了课，却因为临时原因，不能上原来备的课，怎么处理？</h3>
                    <p>在课程开始前，或者课程结束24小时内，您可以在PC端教师系统做以下操作：PIV4.0-学员轨迹-学员名字-清除讲义，之后重新匹配讲义即可。或者使用3.0核录课时。</p>
                </li>

            </ul>
            <ul id="con_one_3" style="display:none;">
            	<li>
                	<h3>1. 为什么我上完课了后，去微信端核录，却没有显示记录轨迹的画面？</h3>
                    <p>如果您没有备课，或者备完课没有把讲义匹配到学生的课次上，是没有办法记录轨迹的。所以请您记得在课前备课，并且记得匹配讲义。</p>
                </li>
                <li>
                	<h3>2. 我已经用4.0核录了，为何显示我的课时逾期了？</h3>
                    <p>可能是您在核录时把例题都点了“未用”，为了避免此类情况，请核录时至少保证使用一道例题。</p>
                </li>
                <li>
                	<h3>3. 3.0或者4.0都已经确认核录成功了，4.0突然蹦出来已经核录过的课时，且显示逾期，我该怎么办？</h3>
                    <p>请您直接在群中，将学员的姓名和上课时间，发到微信群里。只要业务系统确认核录，不会影响到您的工资，请放心。</p>
                </li>
                <li>
                	<h3>4. 我记录出的报告，家长可以通过什么渠道看见？</h3>
                    <p>家长可以通过学员App查看。</p>
                </li>
                <li>
                	<h3>5. 生成的学习报告为什么在学员App不能显示？</h3>
                    <p>这部分问题一般出现在iOS的学员App上出现，是由于数据调取错误造成的。遇到此问题，请直接在群中呼叫洪莲老师或者毛雪松老师就可以了。</p>
                </li>
            </ul>
